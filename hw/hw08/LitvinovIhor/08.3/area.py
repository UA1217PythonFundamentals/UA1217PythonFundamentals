import math

def area_of_rectangle(l, w):
    """ Calculate the area of a rectangle."""
    return l*w
def area_of_triangle(b, h):
    """Calculate the area of a triangle."""
    return (1/2)*b*h
def area_of_circle(r): 
    """Calculate the area of a circle."""
    return round(math.pi*(pow(r,2)),2)